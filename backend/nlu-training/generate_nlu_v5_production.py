# ============================================================
# GENERATE FINAL TRAINING FILE
# ============================================================

def generate_training_data():
    all_samples = []
    stats = {}

    for intent, data in TRAINING_DATA.items():
        examples = data["examples"]
        stats[intent] = len(examples)
        for text in examples:
            all_samples.append({
                "text": text.strip(),
                "intent": intent,
            })

    # Shuffle
    random.shuffle(all_samples)

    # Write
    output_path = os.path.join(os.path.dirname(__file__), "nlu_final_v5_production.jsonl")
    with open(output_path, "w") as f:
        for sample in all_samples:
            f.write(json.dumps(sample, ensure_ascii=False) + "\n")

    print(f"Generated {len(all_samples)} samples across {len(stats)} intents")
    print(f"\nPer-intent distribution:")
    for intent, count in sorted(stats.items(), key=lambda x: -x[1]):
        print(f"  {count:4d}  {intent}")

    # Also write intent mapping (canonical ‚Üí aliases)
    mapping_path = os.path.join(os.path.dirname(__file__), "intent_mapping_v5.json")
    mapping = {}
    for intent, data in TRAINING_DATA.items():
        mapping[intent] = {
            "description": data["description"],
            "aliases": data["aliases"],
            "sample_count": stats[intent],
        }
    with open(mapping_path, "w") as f:
        json.dump(mapping, f, indent=2, ensure_ascii=False)

    print(f"\nFiles written:")
    print(f"  {output_path}")
    print(f"  {mapping_path}")

if __name__ == "__main__":
    generate_training_data()
